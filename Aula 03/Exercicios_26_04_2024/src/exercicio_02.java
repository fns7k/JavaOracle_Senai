
public class exercicio_02 {

	public static void main(String[] args) {
		// 02 - Crie um programa em Java que calcule e mostre a área e o perímetro de um quadrado com lado de valor 15.
		
		// Cálculo da área de um quadrado
				// Criar as variáveis para cálculo
				int lado = 15;
				
				// Área = lado * lado
				int area;
				area = lado * lado;
				System.out.println("O valor da área do quadrado é: " + area);
				
				// Perimetro = 4 * lado
				int perimetro = 4 * lado;
				System.out.println("O valor do perimetro é: " + perimetro);

	}

}
