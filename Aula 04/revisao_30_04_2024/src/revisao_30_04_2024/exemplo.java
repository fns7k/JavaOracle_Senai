package revisao_30_04_2024;

public class exemplo {

	public static void main(String[] args) {
		// Cálculo de area e perimetro do circulo
		
		// areaCirculo = raio² * PI
		// pi = 3,14
		double areaCirculo;
		double raio = 6;
		areaCirculo = raio * raio * 3.14;
		System.out.println("A area do circulo é: " + areaCirculo);
		
		// perimetro = 2 * pi * raio
		double perimetro; 
		perimetro = 2 * 3.14 * raio;
		System.out.println("O valor do perimetro é: " + perimetro);
		
	}
}
