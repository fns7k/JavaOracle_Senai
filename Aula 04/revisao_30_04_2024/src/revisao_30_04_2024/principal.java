		// Criar as variáveis para cálculo
		int lado = 15;
		
		// Área = lado * lado
		int area;
		area = lado * lado;
		System.out.println("O valor da área do quadrado é: " + area);
		
		// Perimetro do quadrado
		// Perimetro = 4 * lado
		int perimetro = 4 * lado;
		System.out.println("O valor do perimetro é: " + perimetro);
		
		

		
	
